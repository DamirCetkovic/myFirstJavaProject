package w1MyExercise;

public class Uppgift9 {
    public static void main(String[] args) {

        // Definerad variabel för cirkels radie
        double radien = 2.17;
        double pi = 3.14;

        //Uträkning av cirkels diameter
        double diameter = 2 * radien;
        System.out.println("Cirkels diameter är " + diameter);
        System.out.println();

        //Uträkning av cirkels omkrets
        double omkrets = 2 * pi * radien;
        System.out.println("Cirkels omkrets är " + omkrets);
        System.out.println();

        //Uträkning för arean
        double area = pi * radien * radien;
        System.out.println("Cirkels area är " + area);


    }
}
