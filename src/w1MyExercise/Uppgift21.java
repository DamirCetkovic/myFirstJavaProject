package w1MyExercise;

public class Uppgift21 {
    public static void main(String[] args) {

        int tal = 9;

        if (tal < -10) {
            System.out.println("Talet är mindr än -10!");
        } else if (tal > 10) {
            System.out.println("Talet är större än 10!");
        } else {
            System.out.println("Talet är mellan -10 0ch 10.");
        }
    }
}
